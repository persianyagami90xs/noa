#include <string.h>

char *
strncat(char *restrict _s1, const char *restrict s2, size_t n)
{
	char *s1;

	if (n != 0) {
		s1 = _s1 + strlen(_s1);
		do {
			if ((*s1 = *s2++) == '\0')
				break;
			s1++;
		} while (--n > 0);
		*s1 = '\0';
	}
	return (_s1);
}
